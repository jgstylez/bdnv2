/**
 * Get mock product by ID
 */
export const getMockProduct = (id: string): Product | undefined => {
  return mockProducts.find((p) => p.id === id);
};

/**
 * Get products by merchant ID
 */
export const getProductsByMerchant = (merchantId: string): Product[] => {
  return mockProducts.filter((p) => p.merchantId === merchantId);
};

/**
 * Get products by category
 * Uses standardized category names for comparison
 */
export const getProductsByCategory = (category: string): Product[] => {
  const normalizedCategory = getStandardizedCategory(category);
  return mockProducts.filter((p) => {
    const normalizedProductCategory = getStandardizedCategory(p.category);
    return normalizedProductCategory === normalizedCategory;
  });
};

/**
 * Get products by type
 */
export const getProductsByType = (type: "physical" | "digital" | "service"): Product[] => {
  return mockProducts.filter((p) => p.productType === type);
};

